package reservadevuelos;

public class Aeropuerto {

    //Atributos
    private String nombre;
    private String ciudad;
    private String pais;

    //Relaciones

    //Constructor
    public Aeropuerto(String nombre, String ciudad, String pais) {
        this.nombre = nombre;
        this.ciudad = ciudad;
        this.pais = pais;
    }
}
